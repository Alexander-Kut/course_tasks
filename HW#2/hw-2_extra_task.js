let number = Number(prompt("Please, enter any number:", "7"));
let numberDoubled = number * 2;
alert("Your doubled number is: " + numberDoubled);

let numberAdded = numberDoubled + 10;
alert(`Your added (+10) number is: ${numberAdded}`);

let numberDivided = numberAdded / 2;
alert(`Divided number is: ${numberDivided}`);

let numberSubtracted = numberDivided - number;
alert(`Subtracted number is: ${numberSubtracted}`);

alert ("Your number is 5!");
